public class task16b_caudilla_arron {
    public static void main(String[] args) {

        int[] numbers = {10, 20, 30, 40, 50};

        // reverse for loop system. 4 to 0
        for (int i = numbers.length - 1; i >= 0; i--) {
            System.out.print(numbers[i] + " ");
        }

        System.out.println();
    }
}